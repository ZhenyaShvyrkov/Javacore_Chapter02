import java.util.LinkedHashSet;

public class LinkedHashSetDemo {
    public static void main(String[] args) {
            LinkedHashSet<String> set = new LinkedHashSet<String>();
            set.add("Альфа");
            set.add("Бета");
            set.add("Эта");
            set.add("Гамма");
            set.add("Эпсилон");
            set.add("Омега");

            System.out.println(set);
    }
}
